const calculator = {
    add: function(a, b) {
        console.log(a + b);
    },
    minus: function(a, b) {
        console.log(a - b);
    },
    div: function(a, b) {
        console.log(a / b);
    },
    multi: function(a, b) {
        console.log(a * b);
    },
    powerof: function(a, b) {
        console.log(a ** b);
    }
};

calculator.add(4, 2);
calculator.minus(4, 2);
calculator.div(4, 2);
calculator.multi(4, 2);
calculator.powerof(4, 2);